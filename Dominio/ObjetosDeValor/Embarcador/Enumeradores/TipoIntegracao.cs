                case TipoIntegracao.BrasilRiskGestao:
                case TipoIntegracao.Infolog:
                case TipoIntegracao.Tecnorisk:
                case TipoIntegracao.ApisulLog:
                case TipoIntegracao.LogRisk:
                case TipoIntegracao.BrasilRiskSML:
                case TipoIntegracao.TransSat:
                case TipoIntegracao.VSTrack:
                    return CategoriaIntegracao.GerenciadoraRisco;

                case TipoIntegracao.OnixSat:
                case TipoIntegracao.Trafegus:
                case TipoIntegracao.Sascar:
                case TipoIntegracao.Omnilink:
                case TipoIntegracao.Autotrac:
