var BuscarProdutoEmbarcadorLote = function (knout, callbackRetorno) {

    var idDiv = guid();
    var GridConsulta;

    var OpcoesKnout = function () {
        this.Titulo = PropertyEntity({ text: "Buscar Lote", type: types.local });
        this.TituloGrid = PropertyEntity({ text: "Lotes", type: types.local });
        this.Descricao = PropertyEntity({ col: 12, text: "Descrição: ", maxlength: 250 });
        this.CodigoBarras = PropertyEntity({ col: 6, text: "Cód. Barras: ", maxlength: 250 });        
        this.Numero = PropertyEntity({ col: 6, text: "Número Lote: ", maxlength: 250 });
        this.Ativo = PropertyEntity({ val: ko.observable(true), def: true, visible: false, text: "Situação: " });
        
        this.Pesquisar = PropertyEntity({
            eventClick: function (e) {
                GridConsulta.CarregarGrid();
            }, type: types.event, text: "Pesquisar", visible: true
        });
    }

    var knoutOpcoes = new OpcoesKnout();

    var divBusca = new CriarBusca(idDiv, knoutOpcoes, knout);


    var callback = function (e) {
        divBusca.DefCallback(e);
    }

    if (callbackRetorno != null) {
        callback = function (e) {
            callbackRetorno(e);
            divBusca.CloseModal();
        }
    }

    GridConsulta = new GridView(idDiv + "_tabelaEntidades", "ProdutoEmbarcador/PesquisaLote", knoutOpcoes, divBusca.OpcaoPadrao(callback), null);
    divBusca.AddEvents(GridConsulta);

    divBusca.AddTabPressEvent(function (outraPressionada) {
        if (outraPressionada) {
            knoutOpcoes.Descricao.val(knout.val());
        }
        GridConsulta.CarregarGrid(function (lista) {
            if (lista.data.length == 1) {
                callback(lista.data[0]);
            } else {
                divBusca.OpenModal();
            }
        });
    })
}